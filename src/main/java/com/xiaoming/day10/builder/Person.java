package com.xiaoming.day10.builder;

/**
 * @Author:         xiaoming
 * @CreateDate:     2019/7/7 18:05
 * @Description:    游戏中的人整体
 */
public class Person {

    /**
     * 头部
     */
    private String head;

    /**
     * 身体
     */
    private String body;

    /**
     * 脚
     */
    private String foot;


    public String getHead() {
        return head;
    }

    public void setHead(String head) {
        this.head = head;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getFoot() {
        return foot;
    }

    public void setFoot(String foot) {
        this.foot = foot;
    }
}
